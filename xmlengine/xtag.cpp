#include "xtag.h"

void XXMLElem::SetByte(unsigned int _pos, unsigned int _len)
{
	pos = _pos;
	len = _len;
}

//______________________________________________________________________________
//------------------------------------------------------------------------------
unsigned int XXMLElem::GetPos()
{
	return pos;
}

//______________________________________________________________________________
//------------------------------------------------------------------------------
unsigned int XXMLElem::GetLen()
{
	return len;
}

//______________________________________________________________________________
//------------------------------------------------------------------------------
XXMLElem::tElem XXMLElem::GetType()
{
	return type;
}

