//---------------------------------------------------------------------------
GWordRef::GWordRef(void)
  : Id(cNoRef)
{
}


//---------------------------------------------------------------------------
GWordRef::GWordRef(unsigned int id)
  : Id(id)
{
}


//---------------------------------------------------------------------------
int GWordRef::Compare(const GWordRef& ref)
{
  return(Id-ref.Id);
}


//---------------------------------------------------------------------------
int GWordRef::Compare(GWordRef *ref)
{
  return(Id-ref->Id);
}


